import UIKit

extension UIView {
    func createLine() -> UIView {
        let view = UIView()
        view.backgroundColor = UIColor(red: 0.741,
                                       green: 0.741,
                                       blue: 0.741,
                                       alpha: 1)
        return view
    }
}
